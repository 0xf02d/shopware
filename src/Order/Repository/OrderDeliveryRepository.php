<?php declare(strict_types=1);

namespace Shopware\Order\Repository;

use Shopware\Api\Read\EntityReaderInterface;
use Shopware\Api\RepositoryInterface;
use Shopware\Api\Search\AggregationResult;
use Shopware\Api\Search\Criteria;
use Shopware\Api\Search\EntityAggregatorInterface;
use Shopware\Api\Search\EntitySearcherInterface;
use Shopware\Api\Search\UuidSearchResult;
use Shopware\Api\Write\EntityWriterInterface;
use Shopware\Api\Write\GenericWrittenEvent;
use Shopware\Api\Write\WriteContext;
use Shopware\Context\Struct\TranslationContext;
use Shopware\Order\Collection\OrderDeliveryBasicCollection;
use Shopware\Order\Collection\OrderDeliveryDetailCollection;
use Shopware\Order\Definition\OrderDeliveryDefinition;
use Shopware\Order\Event\OrderDelivery\OrderDeliveryAggregationResultLoadedEvent;
use Shopware\Order\Event\OrderDelivery\OrderDeliveryBasicLoadedEvent;
use Shopware\Order\Event\OrderDelivery\OrderDeliveryDetailLoadedEvent;
use Shopware\Order\Event\OrderDelivery\OrderDeliverySearchResultLoadedEvent;
use Shopware\Order\Event\OrderDelivery\OrderDeliveryUuidSearchResultLoadedEvent;
use Shopware\Order\Struct\OrderDeliverySearchResult;
use Symfony\Component\EventDispatcher\EventDispatcherInterface;

class OrderDeliveryRepository implements RepositoryInterface
{
    /**
     * @var EntityReaderInterface
     */
    private $reader;

    /**
     * @var EntityWriterInterface
     */
    private $writer;

    /**
     * @var EntitySearcherInterface
     */
    private $searcher;

    /**
     * @var EntityAggregatorInterface
     */
    private $aggregator;

    /**
     * @var EventDispatcherInterface
     */
    private $eventDispatcher;

    public function __construct(
        EntityReaderInterface $reader,
        EntityWriterInterface $writer,
        EntitySearcherInterface $searcher,
        EntityAggregatorInterface $aggregator,
        EventDispatcherInterface $eventDispatcher
    ) {
        $this->reader = $reader;
        $this->writer = $writer;
        $this->searcher = $searcher;
        $this->aggregator = $aggregator;
        $this->eventDispatcher = $eventDispatcher;
    }

    public function search(Criteria $criteria, TranslationContext $context): OrderDeliverySearchResult
    {
        $uuids = $this->searchUuids($criteria, $context);

        $entities = $this->readBasic($uuids->getUuids(), $context);

        $aggregations = null;
        if ($criteria->getAggregations()) {
            $aggregations = $this->aggregate($criteria, $context);
        }

        $result = OrderDeliverySearchResult::createFromResults($uuids, $entities, $aggregations);

        $event = new OrderDeliverySearchResultLoadedEvent($result);
        $this->eventDispatcher->dispatch($event->getName(), $event);

        return $result;
    }

    public function aggregate(Criteria $criteria, TranslationContext $context): AggregationResult
    {
        $result = $this->aggregator->aggregate(OrderDeliveryDefinition::class, $criteria, $context);

        $event = new OrderDeliveryAggregationResultLoadedEvent($result);
        $this->eventDispatcher->dispatch($event->getName(), $event);

        return $result;
    }

    public function searchUuids(Criteria $criteria, TranslationContext $context): UuidSearchResult
    {
        $result = $this->searcher->search(OrderDeliveryDefinition::class, $criteria, $context);

        $event = new OrderDeliveryUuidSearchResultLoadedEvent($result);
        $this->eventDispatcher->dispatch($event->getName(), $event);

        return $result;
    }

    public function readBasic(array $uuids, TranslationContext $context): OrderDeliveryBasicCollection
    {
        /** @var OrderDeliveryBasicCollection $entities */
        $entities = $this->reader->readBasic(OrderDeliveryDefinition::class, $uuids, $context);

        $event = new OrderDeliveryBasicLoadedEvent($entities, $context);
        $this->eventDispatcher->dispatch($event->getName(), $event);

        return $entities;
    }

    public function readDetail(array $uuids, TranslationContext $context): OrderDeliveryDetailCollection
    {
        /** @var OrderDeliveryDetailCollection $entities */
        $entities = $this->reader->readDetail(OrderDeliveryDefinition::class, $uuids, $context);

        $event = new OrderDeliveryDetailLoadedEvent($entities, $context);
        $this->eventDispatcher->dispatch($event->getName(), $event);

        return $entities;
    }

    public function update(array $data, TranslationContext $context): GenericWrittenEvent
    {
        $affected = $this->writer->update(OrderDeliveryDefinition::class, $data, WriteContext::createFromTranslationContext($context));
        $event = GenericWrittenEvent::createFromWriterResult($affected, $context, []);
        $this->eventDispatcher->dispatch(GenericWrittenEvent::NAME, $event);

        return $event;
    }

    public function upsert(array $data, TranslationContext $context): GenericWrittenEvent
    {
        $affected = $this->writer->upsert(OrderDeliveryDefinition::class, $data, WriteContext::createFromTranslationContext($context));
        $event = GenericWrittenEvent::createFromWriterResult($affected, $context, []);
        $this->eventDispatcher->dispatch(GenericWrittenEvent::NAME, $event);

        return $event;
    }

    public function create(array $data, TranslationContext $context): GenericWrittenEvent
    {
        $affected = $this->writer->insert(OrderDeliveryDefinition::class, $data, WriteContext::createFromTranslationContext($context));
        $event = GenericWrittenEvent::createFromWriterResult($affected, $context, []);
        $this->eventDispatcher->dispatch(GenericWrittenEvent::NAME, $event);

        return $event;
    }
}
